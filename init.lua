  -- "gc" to comment visual regions/lines
  {
    'numToStr/Comment.nvim',
    event = 'InsertEnter',
    opts = {
      toggler = {
        ---Line-comment toggle keymap
        line = '<c-/>',
        -- -Block-comment toggle keymap
      },
    },
  },

  -- Import the user-installed plugins
  { import = 'custom/plugins' },
}

